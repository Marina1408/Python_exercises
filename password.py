import re

password_list = str(input(
	  "Input your passwords through comma for check, please!\n > ")).split(', ')
print(password_list)
sample_password = re.compile('([A-Z]{1,2}[a-z]{1,2}[0-9]{1,2}[@#+*]{1,2})|'
                             '([A-Z]{1,2}[a-z]{1,2}[@#+*]{1,2}[0-9]{1,2})|'
                             '([A-Z]{1,2}[0-9]{1,2}[a-z]{1,2}[@#+*]{1,2})|'
                             '([A-Z]{1,2}[0-9]{1,2}[@#+*]{1,2}[a-z]{1,2})|'
                             '([A-Z]{1,2}[@#+*]{1,2}[0-9]{1,2}[a-z]{1,2})|'
                             '([A-Z]{1,2}[@#+*]{1,2}[a-z]{1,2}[0-9]{1,2})|'
                             '([a-z]{1,2}[A-Z]{1,2}[0-9]{1,2}[@#+*]{1,2})|'
                             '([a-z]{1,2}[A-Z]{1,2}[@#+*]{1,2}[0-9]{1,2})|'
                             '([a-z]{1,2}[0-9]{1,2}[@#+*]{1,2}[A-Z]{1,2})|'
                             '([a-z]{1,2}[0-9]{1,2}[A-Z]{1,2}[@#+*]{1,2})|'
                             '([a-z]{1,2}[@#+*]{1,2}[0-9]{1,2}[A-Z]{1,2})|'
                             '([a-z]{1,2}[@#+*]{1,2}[A-Z]{1,2}[0-9]{1,2})|'
                             '([@#+*]{1,2}[0-9]{1,2}[A-Z]{1,2}[a-z]{1,2})|'
                             '([@#+*]{1,2}[0-9]{1,2}[a-z]{1,2}[A-Z]{1,2})|'
                             '([@#+*]{1,2}[A-Z]{1,2}[0-9]{1,2}[a-z]{1,2})|'
                             '([@#+*]{1,2}[A-Z]{1,2}[a-z]{1,2}[0-9]{1,2})|'
                             '([@#+*]{1,2}[a-z]{1,2}[0-9]{1,2}[A-Z]{1,2})|'
                             '([@#+*]{1,2}[a-z]{1,2}[A-Z]{1,2}[0-9]{1,2})|'
                             '([0-9]{1,2}[@#+*]{1,2}[A-Z]{1,2}[a-z]{1,2})|'
                             '([0-9]{1,2}[@#+*]{1,2}[a-z]{1,2}[A-Z]{1,2})|'
                             '([0-9]{1,2}[A-Z]{1,2}[@#+*]{1,2}[a-z]{1,2})|'
                             '([0-9]{1,2}[A-Z]{1,2}[a-z]{1,2}[@#+*]{1,2})|'
                             '([0-9]{1,2}[a-z]{1,2}[A-Z]{1,2}[@#+*]{1,2})|'
                             '([0-9]{1,2}[a-z]{1,2}[@#+*]{1,2}[A-Z]{1,2})')

i = 0

for password in password_list:
	check = sample_password.match(password)
	if check and len(password) <= 6:
		print('Your password {} is correct.'.format(password))
	else:
		i += 1

if i == len(password_list):
	print('You all passwords are incorrect!')
